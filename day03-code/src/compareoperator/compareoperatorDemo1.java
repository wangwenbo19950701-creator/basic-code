package compareoperator;

public class compareoperatorDemo1 {
    public static void main(String[] args) {
        //比较运算符有==，!=，>，<，>=，<=，返回的结果不是true就是false
        int a = 10;
        int b = 10;
        int c = 20;
        System.out.println(a == b);//true
        System.out.println(a == c);//false
        //注意，==和=是不一样的，==是比较，=是赋值
    }
}
